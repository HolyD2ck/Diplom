                        'рейтинг' => $reviewData['рейтинг'],
                        'отзыв' => $reviewData['отзыв'],
                        'товар_id' => $product->id,
                        'пользователь_id' => $this->случайныйПользователь(),
                    ]);
                }
            }
        }
    }
